class Solution:
    def maxProduct(self, nums: List[int]) -> int:
        dpMax, dpMin = [], []
        dpMax.append(nums[0])
        dpMin.append(nums[0])

        for i in range(1,len(nums)):
            dpMax.append(max(dpMax[i-1]*nums[i], dpMin[i-1]*nums[i], nums[i]))
            dpMin.append(min(dpMax[i-1]*nums[i], dpMin[i-1]*nums[i], nums[i]))
        return max(dpMax)